#Universal Method in Tuple
a = (10, 20, 30, 40, 50)
print("Original Tuple:", a)
print("Length of Tuple:", len(a))           # Length of tuple
print("Maximum value in Tuple:", max(a))    # Maximum value
print("Minimum value in Tuple:", min(a))    # Minimum value
print("Sum of all elements in Tuple:", sum(a))  # Sum of elements
print("Sorted Tuple:", sorted(a))           # Sorted tuple
print("Count of 30 in Tuple:", a.count(30)) # Count occurrences of 30
print("Index of 40 in Tuple:", a.index(40)) # Index of first occurrence of 40
