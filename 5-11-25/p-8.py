#Universal method in list 
#len(), min(), max(), sum(),sorted(),sorted(reverse=True)
my_list = [10, 20, 30, 40, 50]
print("Original List:", my_list)
print("Length of List:", len(my_list))
print("Minimum Element:", min(my_list))
print("Maximum Element:", max(my_list))
print("Sum of Elements:", sum(my_list))
print("Sorted List:", sorted(my_list))
print("Sorted List in Reverse Order:", sorted(my_list, reverse=True))
